# Manejo de datos numericos en python

# int / Enteros
lives = 3
print(type(lives))
print(lives)

# float / Flotante o decimal
temperature = 12.12
print(type(temperature))

#incremento y decremento de valores += / -=
lives += 20
print(lives)

lives -= 2
print(lives)

# Formatos de numeros largos

numero_largo = 45000000000000000000000000000.1
